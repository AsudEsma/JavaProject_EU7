package classes.day03_variables_dataTypes;

public class ConstantVeriables {

	public static void main(String[] args) {
	
		final double PI = 3.14;
		final String BIRTHDAY = "13/07/1993";
				
		System.out.println(PI);
		System.out.println(BIRTHDAY);
		
		int capacity = 256;
		int numbers = 258_964;
		
		System.out.println(capacity + numbers);
		System.out.println(capacity * numbers);
		
		String WhatsYourName = "Ahmet";
		
		System.out.println("What is your name?\t" + WhatsYourName + ".");
	
	
	
	}
	
	

}
