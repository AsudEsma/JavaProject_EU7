package classes.day27_arrays;

public class AssigningTo_2D_Arrays {

	public static void main(String[] args) {

		int [][] numbers = {{1,2,3,4}, {6,7,8,9}, {11,12,13,14}};

		System.out.println("First row has:");
		System.out.println(numbers[0][0]);
		System.out.println(numbers[0][1]);
		System.out.println(numbers[0][2]);
		System.out.println(numbers[0][3]);
		
		System.out.println("Second row has:");
		System.out.println(numbers[1][0]);
		System.out.println(numbers[1][1]);
		System.out.println(numbers[1][2]);
		System.out.println(numbers[1][3]);
		
		System.out.println("Third row has:");
		System.out.println(numbers[2][0]);
		System.out.println(numbers[2][1]);
		System.out.println(numbers[2][2]);
		System.out.println(numbers[2][3]);
		
		
		String[][] names = {{"Zoi", "Koi", "Kali"}, {"Theo", "Teemo", "Tai", "Tha"}, {"Mai", "Mei", "Tulu"}};
		System.out.println("First row has:");
		System.out.println(names[0][0]);
		System.out.println(names[0][1]);
		System.out.println(names[0][2]);
		
		System.out.println("Second row has:");
		System.out.println(names[1][0]);
		System.out.println(names[1][1]);
		System.out.println(names[1][2]);
		System.out.println(names[1][3]);
		
		System.out.println("Third row has:");
		System.out.println(names[2][0]);
		System.out.println(names[2][1]);
		System.out.println(names[2][2]);
	
	
	}

}
