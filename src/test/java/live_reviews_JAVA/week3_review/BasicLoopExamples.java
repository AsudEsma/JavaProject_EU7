package live_reviews_JAVA.week3_review;

public class BasicLoopExamples {

	public static void main(String[] args) {

		for(int i=1; i<=10; i+=2) {
			System.out.print("Hello ");
		}

		System.out.println();
		System.out.println("-------------");
		
		for (int i=1; i<=10; i++) {
			System.out.println("2 x " + i + " = " + 2*i);
		}
	}

}
